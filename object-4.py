
# Object # 04: Write a program that will ask for number of days and then will show how many hours, minutes and seconds are in that number of days

a = input("Enter number of days:")
b = int(a) 
c = b * 24
d = c * 60
e = d * 60

print("The number of hours in the given days will be", c)
print("The number of minutes in the given days will be", d )
print("The number of seconds in the given days will be", e )
